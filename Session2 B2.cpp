#include <stdio.h>

int main() {
    // Khai bao va khoi tao bien hai so nguyen
    int num1 = 25; // Bien so nguyen thu nhat co gia tri la 25
    int num2 = 15; // Bien so nguyen thu hai co gia tri la 15

    // Khai bao bien luu ket qua va tinh tong
    int sum = num1 + num2; // Tinh tong num1 va num2, luu ket qua vao sum

    // In ket qua ra man hinh
    printf("Tong cua %d va %d la: %d\n", num1, num2, sum);

    return 0;
}

